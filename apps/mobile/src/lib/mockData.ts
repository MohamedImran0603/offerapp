export const generateMockFlyers = () => {
  const mockDists = districts.slice(1);
  const allMocks = [];
  
  for (let i = 0; i < 180; i++) {
    const template = uniqueProducts[i % uniqueProducts.length];
    const brand = brands[i % brands.length].name;
    const dist = mockDists[i % mockDists.length];
    const pages = 4 + Math.floor(Math.random() * 24);
    const daysLeft = 1 + Math.floor(Math.random() * 12);
    const oldPrice = 500 + Math.floor(Math.random() * 10000);
    const discountPercents = [10, 15, 25, 30, 45, 50];
    const offerPrices: { [key: number]: number } = {};
    discountPercents.forEach(perc => {
      offerPrices[perc] = Math.round(oldPrice * (1 - perc / 100));
    });
    
    // Select category-specific images dynamically
    const categoryPics = categoryImages[template.cat] || defaultImages;
    const chosenImage = categoryPics[i % categoryPics.length];

    allMocks.push({
      id: `flyer-mock-${i}`,
      title: template.name,
      subTitle: template.sub || 'Special Offer',
      store: brand,
      district: dist,
      category: template.cat,
      image: `${chosenImage}?auto=format&fit=crop&q=80&w=800`,
      brand,
      pages,
      daysLeft,
      oldPrice,
      offerPrices,
      isTop: i < 20,
    });
  }
  return allMocks;
};

export const mockData = generateMockFlyers();
